    public void mostrarDatos() {
        
        DefaultTableModel _model = (DefaultTableModel)this.tblJuegos.getModel();
        
        _model.setRowCount(0);
        
        for(Juegos _jue : frmPrincipal._catalogoJuegos.listaJuegos()) {
            
            _model.addRow(new Object[]{_jue.getNombre(), 
                _jue.getCodigo(), 
                _jue.getPrecioRenta(), 
                _jue.getPrecioCompra(),
                _jue.getStock(),
                _jue.getMarca(),
                _jue.getPlataforma()}); 
        
        }
        
        this.tblJuegos.setModel(_model);
    }

